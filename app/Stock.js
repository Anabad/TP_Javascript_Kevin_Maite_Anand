'use strict'

class Stock {

    constructor() {
        for (i < 0; i < NBINGREDIENTS; i++) {
            ingredient
                [i] = getRandom(20, 30);
            max
                [i] = ingredient[i];
        }
    }

    ravitaille() {
        for (i < 0; i < NBINGREDIENTS; i++) {
            ingredient[i] = max[i];
        }
    }
}
